# Функция для поиска проекта по названию
def find_project(project_name, projects):
    project_name_lower = project_name.lower()
    for key, value in projects.items():
        if key in project_name_lower or project_name_lower in key:
            return value
        if value.get("название", "").lower() in project_name_lower:
            return value
    return None

# Функция для генерации ответа на возражение
def generate_response(project_info, objection, knowledge_base):
    prompt = f"""
    Ты помощник рекрутера в аутсорсинговом агентстве. 
    Твоя задача - помочь рекрутеру отработать возражение кандидата.
    
    Информация о проекте:
    {json.dumps(project_info, ensure_ascii=False, indent=2)}
    
    База знаний по работе с возражениями:
    {knowledge_base}
    
    Возражение кандидата: "{objection}"
    
    Сформулируй ответ для рекрутера, который поможет отработать это возражение.
    Ответ должен быть:
    1. Конкретным и привязанным к данным проекта
    2. Дружелюбным и убедительным
    3. Содержать конкретные аргументы из преимуществ проекта
    4. Не выдумывать информацию, которой нет в данных проекта
    
    Если информации в проекте недостаточно для ответа, честно скажи об этом и предложи общий шаблон ответа.
    
    Формат ответа:
    - Краткий анализ возражения
    - Конкретные аргументы из проекта
    - Готовый текст для разговора с кандидатом
    """
    
    try:
        response = model.generate_content(prompt)
        return response.text
    except Exception as e:
        return f"Ошибка при генерации ответа: {str(e)}"

